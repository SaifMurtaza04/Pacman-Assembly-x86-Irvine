File_Handle PROC
    ; Create the file and store the handle
    mov edx, OFFSET HSFILE
    call CreateOutputFile
    mov filehandle, eax

    ; Convert score to string (right-aligned into SCOREFFILE)
    mov eax, 0
    mov al, score
    mov cx, 10
    mov esi, OFFSET SCOREFFILE + 9  ; rightmost digit position

convert_loop:
    xor dx, dx
    div cx
    add dl, '0'
    dec esi
    mov [esi], dl
    test ax, ax
    jnz convert_loop

    ; Write "Score: " with space padding for alignment
    mov eax, filehandle
    mov edx, OFFSET HS_label1
    mov ecx, LENGTHOF HS_label1 - 1
    call WriteToFile

    ; Write SCOREFFILE (right-aligned score string)
    mov ecx, OFFSET SCOREFFILE + 10
    sub ecx, esi
    mov eax, filehandle
    mov edx, esi
    call WriteToFile

    ; Add padding (3 spaces) after Score to ensure alignment
    mov eax, filehandle
    mov edx, OFFSET HSspace
    mov ecx, LENGTHOF HSspace - 1
    call WriteToFile

    ; Write "Name: "
    mov eax, filehandle
    mov edx, OFFSET HS_label2
    mov ecx, LENGTHOF HS_label2 - 1
    call WriteToFile

    ; Write Pname (the player's name)
    mov eax, filehandle
    mov edx, OFFSET Pname
    mov ecx, BUFNSIZE
    call WriteToFile

    ; Add padding (3 spaces) after Name to ensure alignment
    mov eax, filehandle
    mov edx, OFFSET HSspace
    mov ecx, LENGTHOF HSspace - 1
    call WriteToFile

    ; Write "Level: "
    mov eax, filehandle
    mov edx, OFFSET HS_label3
    mov ecx, LENGTHOF HS_label3 - 1
    call WriteToFile

    ; Convert levelNumber (byte) to character and store in LEVELBUF
    movzx eax, levelNumber
    add al, '0'
    mov LEVELBUF[7], al

    ; Write the level number in LEVELBUF, after "Level: "
    mov eax, filehandle
    mov edx, OFFSET LEVELBUF + 7
    mov ecx, 1  ; Write only 1 character (level number)
    call WriteToFile


    ret
File_Handle ENDP


